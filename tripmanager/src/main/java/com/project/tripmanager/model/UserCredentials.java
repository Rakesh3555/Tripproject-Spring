package com.project.tripmanager.model;

public class UserCredentials {
	
	String username;
	String password;
	String mail;
	String generaterOtp;
	String mobileNumber;
	String valOtp;
	String tripPackage;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getGeneraterOtp() {
		return generaterOtp;
	}
	public void setGeneraterOtp(String generaterOtp) {
		this.generaterOtp = generaterOtp;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getValOtp() {
		return valOtp;
	}
	public void setValOtp(String valOtp) {
		this.valOtp = valOtp;
	}
	public String getTripPackage() {
		return tripPackage;
	}
	public void setTripPackage(String tripPackage) {
		this.tripPackage = tripPackage;
	}
	
	

}
